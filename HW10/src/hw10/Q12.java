public class Q12 {

	public static void main(String[] args) {
		long bigNum  = 1234;    // Auto promoted to long.
	  //long bigNum2 = 1234.0;  // Double cannot be converted to long.
	  //long bigNum3 = 1234.0L; // Invalid syntax, floating point given, with 'L' postfix
		long bigNum4 = 1234l;   // Both 'L', and lowercase 'l' can be used.
		long bigNum5 = 1234L;   // Valid long value.
		long bigNum6 = 1_234;   // Underscores are valid to insert between numbers for the sake
								// readability of big numbers.
		
		
		
	}

}
