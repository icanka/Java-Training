public class Q26 {

	// Instance variables automatically initializes with default values
	// decimal values have 0.0 as default value
	static double defaultValue;

	// Integer values defaults to 0.
	static int defaultValue2;
	static long defaultValue3;
	static short defaultValue4;

	public static void main(String[] args) {

		System.out.println(defaultValue);
		System.out.println(defaultValue2);
		System.out.println(defaultValue3);
		System.out.println(defaultValue4);
	}

}
